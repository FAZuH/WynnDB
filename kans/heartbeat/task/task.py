from typing import Protocol


class Task(Protocol):
    """<<interface>>"""
    def setup(self) -> None: ...
    def teardown(self) -> None: ...
    def run(self) -> None: ...
    @property
    def first_delay(self) -> float: ...
    @property
    def interval(self) -> float: ...
    @property
    def name(self) -> str: ...
